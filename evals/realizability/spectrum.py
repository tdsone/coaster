"""Spectral signatures of read-induced coverage.

A read of length L acts as a length-L moving-average filter on the read-start
density, attenuating frequencies above ~1/L. For an insert-length
distribution with minimum length L_min, any spectral energy at frequencies
above 1/L_min cannot be produced by a single read; it can only arise from
correlations between read-start positions, which are typically weak relative
to the kernel's smoothing. A high fraction of energy in this band is
evidence that the profile was not generated by aggregating reads.
"""
from __future__ import annotations

import numpy as np


def high_freq_energy_fraction(cov: np.ndarray, cutoff_period_bp: float) -> float:
    """Fraction of spectral energy at periods shorter than cutoff_period_bp.

    Implemented via rFFT on the mean-subtracted profile (DC removed).
    """
    cov = np.asarray(cov, dtype=np.float64)
    if cov.size < 4:
        return float("nan")
    x = cov - cov.mean()
    f = np.fft.rfft(x)
    power = (f.real**2 + f.imag**2)
    total = power.sum() + 1e-12
    # FFT bin k corresponds to frequency k / N (cycles per bp), i.e. period N/k bp.
    n = cov.size
    k = np.arange(power.size)
    period = np.where(k > 0, n / np.maximum(k, 1), np.inf)
    hf_mask = period < cutoff_period_bp
    return float(power[hf_mask].sum() / total)
